public class Terrain {
    private double xmin ;
    private double xmax ;
    private double ymin ;
    private double ymax ;
    
    public Terrain (double xmin, double xmax, double ymin, double ymax) {
        this.xmin = xmin;
        this.xmax = xmax;
        this.ymin = ymin;
        this.ymax = ymax;
    }
}
