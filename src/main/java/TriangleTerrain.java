public class TriangleTerrain {
    public int idT ;
    protected Point PT0;
    protected Point PT1;
    protected Point PT2;
    
    public TriangleTerrain (int idT, Point PT0, Point PT1, Point PT2) {
        this.idT = idT ;
        this.PT0 = PT0;
        this.PT1 = PT1;
        this.PT2 = PT2;
    }
}
